								<a class="image featured"><img src="images/longsor3.jpg" alt="" /></a>
								<p>Awal musim penghujan yang dimulai sejak awal bulan November 2018 mengakibatkan bencana longsor dan angin kencang di Kabupaten Magelang. Tercatat setidaknya terdapat 16 bencana tanah longsor, dan lima bencana angin kencang yang terjadi selama tiga pekan terakhir. Seperti bencana tanah longsor yang terjadi di Kecamatan Secang, Kecamatan Kajoran, Kecamatan Salaman, Kecamatan Mertoyudan, Kecamatan Bandongan, Kecamatan Sawangan, dan Kecamatan Ngablak. Desa yang terdampak yakni Pirikan, Bumiayu, Kalisalak, Krinjing, Mertoyudan, Sidosari, Margoyoso, Sambak, Lesanpuro, Ngepanrejo, Podosoko, Ngampeldento, Kanigoro, dan Margoyoso. Untuk angin kencang terjadi di sebanyak sekali di Kecamatan Secang, Kecamatan Tegalrejo sebanyak satu kali, dan Kecamatan Salaman sebanyak tiga kali kejadian. Desa yang terdampak yakni Desa Donomulyo, Kebonagung, Ngadirejo, Krasak, dan Sidosari. "Saat ini telah terjadi 16 longsor yang mengakibatkan kerusakan rumah milik warga, serta menutup akses jalan. Disamping itu, juga terjadi 5 bencana hujan angin yang melanda di Kecamatan Secang, Tegalrejo, dan Salaman," ujar Kepala Pelaksana Badan Penanggulangan Bencana Daerah (BPBD) Kabupaten Magelang, Edi Susanto. Edi mengatakan, bencana yang terjadi tersebut mengakibatkan dampak kerusakan. Dampak bencana yang terjadi dari mulai kerusakan rumah, kerusakan talud atau saluran irigasi, sampai dengan tertutupnya akses jalan akibat tanah longsor.</p>
