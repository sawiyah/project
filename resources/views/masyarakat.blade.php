										<h2><a>BPBD Kota Yogyakarta Minta Masyarakat Jadi Satpam Sungai</a></h2>
										<p>Yogyakarta</p>
									</div>
									<div class="meta">
										<time class="published" datetime="2018-7-30">senin, 3 Juli 2018</time>
									</div>
								</header>
								<a class="image featured"><img src="images/BPBD.jpg" alt="" /></a>
								<p>Hadapi musim hujan, Kepala Pelaksana Badan Penanggulangan Bencana (BPBD) Kota Yogyakarta, Hari Wahyudi meminta masyarakat tepi sungai menjadi satpam sungai. Hari mengatakan yang mengetahui kondisi sungai secara keseluruhan adalah masyarakat yang tinggal di tepi sungai. Menurutnya ketika masyarakat lebih peduli terhadap tanda-tanda, kejadian yang tidak diinginkan bisa diantisipasi. "Pengennya itu warga yang tinggal di tepi sungai jadi satpam sungai. Jadi warga tepi sungai itu sendiri yang menjaga dan mencermati setiap perubahan yang terjadi di sungai. Ibarat rumah sendiri, yang tahu paku lepas kan tuan rumahnya. Sama halnya dengan di sungai," katanya. Ia mengungkapkan, banjir merupakan peristiwa yang terjadi hampir setiap tahun. Meski tidak menimbulkan banyak korban, namun keewaspadaan masyarakat juga diperlukan. "Kalau gempa itu kan peristiwa yang lama sekali, belum terjadi yang besar seperti dulu. Tetapi kalau banjir itu hampir setiap tahun. Meskipun tidak memakan banyak korban, beberapa rumah yang melanggar sepadan sungai itu yang jadi korban." ungkapnya. "Sungai di Yogyakarta itu dalam, jadinya ya kalau banjir tidak akan seperti Jakarta. Nah tetapi kan nanya tepi sungai kan ada tebingnya, ya mungkin saja bisa terjadi longsor," sambungnya. Oleh sebab itu, pihaknya kini fokus pada Kampung Tangguh Bencana (KTB), khususnya yang berada di pinggir sungai.</p>
