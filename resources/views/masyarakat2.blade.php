										<h2><a>Pengunjung Pantai Parangtritis Hilang Tergulung Ombak</a></h2>
										<p>Yogyakarta</p>
									</div>
									<div class="meta">
										<time class="published" datetime="2018-7-7">Selasa, 7 Juli 2018</time>
									</div>
								</header>
								<a class="image featured"><img src="images/ombak.jpg" alt="" /></a>
								<p>Gelombang laut selatan kembali memakan korban. Kali ini seorang pengunjung Pantai Parangtritis, Bantul, Yogyakarta, hilang tergulung ombak saat mandi di laut. Nasrudin, warga Purwodadi Kaliangkrik Magelang Jawa Tengah mengalami nasib naas saat bersama enam rekannya mandi di sisi barat kawasan Parangtritis tepatnya di Pantai Parangkusumo. "Korban mandi bersama teman-temannya di daerah yang kebetulan sepi. Tim SAR kan tidak bisa mengawasi semuanya, jadi tahu-tahu kita mendapat laporan dari temannya", kata Ali Susanto, Komandan SAR Parangtritis saat dikonfirmasi, Minggu (7\/7\/2013). Nasrudin datang ke Pantai Parangtritis sebenarnya hanya untuk mengisi waktu luang di hari Minggu. Pemuda 22 tahun itu beberapa hari terakhir bekerja sebagai buruh bangunan di sebuah proyek kawasan Colombo Yogyakarta. "Cerita temannya, main ke sini (pantai) karena sedang libur. Ini kan hari Minggu, jadi mereka jalan-jalan mengisi waktu", terang Ali. Tiba di Parangtritis sekitar pukul 10.00 WIB, Nasrudin menyusuri bibir pantai ke arah barat hingga sampai di kawasan Parangkusumo. "Mereka lalu mandi di situ hingga akhirnya terseret ombak sekitar pukul 14.15 WIB. Temannya berusaha menolong tapi karena terseret ke tengah, mereka tidak berani", ujarnya. Usai rekan korban melapor ke Posko SAR yang berjarak lebih dari 300 meter di sisi timur lokasi kejadian, Tim SAR akhirnya datang dan berupaya melakukan pencarian. Empat anggota SAR berenang di antara terjangan gelombang membawa dua jaring. Mereka kemudian menebarnya sekitar 200 meter dari bibir pantai. "Yang bisa kita lakukan ya baru dengan jaring eret itu. Kalau menggunakan perahu ke tengah kita belum berani karena terkendala ombak dan angin", katanya. Jaring yang diikat dengan tali plastik panjang itu dikendalikan oleh beberapa anggota SAR dan dibiarkan terbawa arus ke arah barat. Namun hingga petang, upaya pencarian belum juga berhasil menemukan korban.</p>
