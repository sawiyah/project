								<p>ngin puting beliung melanda sebagian wilayah Daerah Istimewa Yogyakarta (DIY). Peristiwa yang terjadi pukul 14.05 WIB tadi berlangsung selama 22 menit. "Puting beliung awal terbentuk pukul 14.05 sampai dengan 14.27 WIB," kata Supervisor Kelompok II Pusdalops BPBD DIY, Indrayanto, dalam keterangan tertulis yang diterima detikcom, Selasa. Berdasarkan laporan sementara dari Pusdalops BPBD DIY, tercatat hingga pukul 15.15 WIB dilaporkan sebanyak 34 rumah rusak di Sorowajan, Banguntapan, Bantul. Dari puluhan rumah itu, 23 rumah berada di RT 3 RW 8 dan 11 rumah di RT 2 RW 8. Selain itu juga dilaporkan pohon tumbang dan kedai kopi rusak. "Titik kedua dilaporkan terjadi pohon tumbang di wilayah kampus STPMD APMD Baciro Gondokusuman. Seorang dosen dilaporkan terluka dibawa ke RS Bethesda," jelas Indrayanto. Sebagian warga di sekitar Kecamatan Gondokusuman, Kota Yogyakarta dan Banguntapan Bantul yang letaknya berbatasan melihat angin kencang tersebut mirip tornado. Angin tersebut bergerak dari arah barat menuju timur dan kemudian ke arah tenggara. Tidak lama setelah ada angin kencang atau puting beliung itu kemudian turun hujan deras di wilayah Yogyakarta. "Kejadiannya tanpa dikira-kira. Dari arah barat muncul angin kayak tornado," ungkap seorang warga, Kabul kepada wartawan di Jalan Ipda Tut Harsono, Timoho Yogyakarta. Rumah tempat tinggal Kabul sekaligus galeri seni yang ada di Jl Ipda Tut Harsono, Timoho No. 313 Yogyakarta itu ikut rusak. Atap seng dan genteng ikut beterbangan. Atap dan sebagian bangunan rusak roboh. Demikian pula beberapa gerobak dan bangunan di sekitar itu juga rusak. Beberapa pohon di kawasan Kampung Gendeng hingga Sorowajan juga tumbang.</p>
