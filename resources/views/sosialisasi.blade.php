								<span class="image featured"><img src="images/siswa.jpg" alt="" /></span>
								<p>Badan Penanggulangan Bencana Daerah (BPBD) Provinsi Lampung menggelar program sosialisasi tanggap bencana ke seluruh sekolah sekolah tahun 2016 ini. Hal itu terungkap dalam Rapat koordinasi (Rakor) penanggulangan bencana provinsi Lampung di aula gedung BPBD setempat pada, Jumat (5/2). Kepala BPBD Lampung, Sena Adhi Winarta didampingi Sekretaris BPBD, Muhammad Fadli mengatakan rapat dilakukan sebagai langkah sinergitas program kegiatan untuk tahun anggaran 2016. BPBD Provinsi dan Kabupaten/kota akan melaksanakan kegiatan sosialisasi ini di sekolah-sekolah terpilih di Lampung, ujarnya. “BPBD melaksanakan kegiatan sosialisasi penanggulangan bencana untuk pelajar. Gunanya untuk membangun kesadaran anak sekolah akan adanya ancaman bencana alam. Jadi saat gempa bumi tidak panik lagi dan tau harus berbuat apa. Seperti berlindung dibawah meja didalam gedung,” Kata Sena saat ditemui diruang kerjanya. Beberapa sekolah itu antara lain berada di Kabupaten Lampung Selatan, Lampung Timur, Pesawaran, dan Tanggamus. “Kalau Lampung Barat dan dan pesisir Barat nanti sosialisasi desa tangguh bencana untuk orang dewasa. Serta pemahaman Kelompok Masyarakat seperti karang taruna dan organisasi kepemudaan. Diharapkan bisa membantu masyarakat melakukan evakuasi ketempat yang lebih tinggi saat bencana tsunami,” Imbuhnya. Selain sosialisasi, BPBD juga akan fokus pada konseling traumatic pasca bencana, “Jadi perlu ada masyarakat baik tokoh maupun aparatur yang telah mengikuti pelatihan dengan psikolog dan psikiater sebagai narasumber. Misalkan ada bencana besar di Krui bisa melakukan pendampingan kepada masyarakat, pemulihan psikis korban bencana.” Jelas Ketua IPSI Lampung ini. Ditahun 2016 ini juga pihaknya membentuk tim teaksi cepat yang terdiri dari beberapa dinas terkait yang punya kepedulian terhadap bencana. Dinas ini diberikan bekal dan teknik untuk melakukan tanggap darurat penanganan bencana, tegasnya. “Posko siaga bencana BPBD juga bekerjasama dengan Rapi tiap malam cek kondisi lapangan melalui jalur radio dan telepon setiap hari. Ada 8 orang yang melakukan pemantauan dan monitoring kejadian bencana di provinsi Lampung setiap harinya,” kata Sena yang juga mantan Kepala Biro Organisasi. Program kedepan ditahun 2017, BPBD kabupaten kota dan provinsi akan mengalokasikan dana untuk kegiatan gladi lapangan dan gladi posko dalam rangka penanggulangan bencana, seperti simulasi bencana tsunami di pantai mutun, pungkasnya</p>
