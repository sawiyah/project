								<span class="image featured"><img src="images/difabel.webp" alt="" /></span>
                                    <p>Berbagai ragam difabel tampak antusias menyimak materi-materi yang disampaikan oleh BPBD (Badan Penanggulangan Bencana Daerah) Kota Surakarta di Taman Balai Kambang Solo. Pada acara  tersebut, para pemateri memaparkan tentang langkah-langkah penyelamatan diri bagi difabel ketika terjadi bencana.“Bagaimana cara kita mengidentifikasi adanya kebakaran?”, tanya salah seorang teman Tuli yang langsung mendapat respon pemateri. Pertanyaan serupa juga muncul dari teman-teman netra, “Lalu bagaimana cara penyelamatan diri yang paling tepat ketika titik api berada di pintu keluar?”. “Ketika terjadi kebakaran, sangat penting bagi kita untuk mengetahui dimana titik apinya. Jika titik api berada di dekat pintu keluar, dan tidak ada jalan keluar lain, maka satu-satunya jalan adalah menerobos titik api tersebut. Untuk mengurangi resiko terbakar, Teman-teman bisa menggunakan selimut atau seprei yang telah dibasahi dengan air lalu dibalutkan ke sekujur tubuh.”, terang Dono Tumpo, salah seorang pemateri dari BPBD Surakarta. Antusiasme peserta semakin bertambah saat para pemateri memaparkan tentang peluang-peluang kinerja kerelawanan yang bisa dilakukan oleh para difabel dalam kebencanaan. “Teman-teman difabel dapat terlibat  dalam pengaturan dan pembagian logistik bagi para korban bencana.”, jelas  Dono dengan yakin. Hal senada juga diungkapkan oleh Fahmi, pemateri lain dari BPBD Surakarta, yang memberikan materi mengenai water treatment. “Di sini, teman-teman difabel dapat berbagi peran. Teman Tuli bisa mengambil air yang akan digunakan, teman-teman berkursi roda dapat mengatur alat pensteril-nya, kemudian teman netra bisa membersihkan selang pembuangan limbah-nya.”, ujarnya. “Seperti yang telah diinformasikan, nanti akan ada pendaftaran relawan bagi teman-teman difabel.”, ungkap Sri Sudarti, salah satu koordinator difabel, di sela-sela sesi. Lebih lanjut, ia mengatakan bahwa sebelum relawan difabel dapat terbentuk, kerja sama antara BPBD dan difabel dapat berbentuk kemitraan. </p>
